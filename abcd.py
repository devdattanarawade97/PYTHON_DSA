




# function for find sum of two numbers

def sum(num1, num2):
    """
    Returns the sum of two numbers.

    Parameters
    ----------
    num1 : int or float
        The first number to be summed.
    num2 : int or float
        The second number to be summed.

    Returns
    -------
    int or float
        The sum of num1 and num2.
    """
    return num1 + num2